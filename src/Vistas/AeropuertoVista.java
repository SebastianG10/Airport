    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed
        AgregarCliente formularioCliente = new AgregarCliente(this, true);
        formularioCliente.setVisible(true);
        String nombre = formularioCliente.getNombre();
        int documento = formularioCliente.getDocumento();
        String direccion = formularioCliente.getDireccion();
        String contraseña = formularioCliente.getContraseña();
        int cuentaBancaria = formularioCliente.getcuentaBancaria();
        String tipo = formularioCliente.getTipo();
        String correo = formularioCliente.getCorreo();
        int telefono = formularioCliente.getTelefono();
        try {
            LaNubia.RegistrarCliente(nombre, documento, direccion, contraseña, cuentaBancaria, tipo, correo, telefono);
        } catch (MisExcepciones ex) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error " + ex);
        }
    }//GEN-LAST:event_btnRegistrarseActionPerformed

    private void btnIniciarSesionClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionClientesActionPerformed
        IniciarSesion formularioIniciarSesion = new IniciarSesion(this, true);
        formularioIniciarSesion.setVisible(true);
        int documento = formularioIniciarSesion.getDocumento();
        String contraseña = formularioIniciarSesion.getContraseña();
        int resultado;
        try {
            resultado = LaNubia.IniciarSesion(documento, contraseña);
            if (resultado == 1) {
                ClientePremium formularioInicioSesionPremium = new ClientePremium(null, true, this.LaNubia);
                formularioInicioSesionPremium.setVisible(true);
            }
            if (resultado == 2) {
                ClienteNormal formularioInicioSesionNormal = new ClienteNormal(null, true, this.LaNubia);
                formularioInicioSesionNormal.setVisible(true);
            }
            if (resultado == 0) {
                JOptionPane.showMessageDialog(this, "Usuario no encontrado registrese");
            }
        } catch (MisExcepciones ex) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error " + ex);
        }

    }//GEN-LAST:event_btnIniciarSesionClientesActionPerformed

    private void btnIniciarSesionAdministradorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionAdministradorActionPerformed
        IniciarSesionAdministrador formularioIniciarSesionAdministrador = new IniciarSesionAdministrador(this, true);
        formularioIniciarSesionAdministrador.setVisible(true);
        String usuario = formularioIniciarSesionAdministrador.getUsuario();
        String contraseña = formularioIniciarSesionAdministrador.getContraseña();
        boolean resultado;
        try {
            resultado = LaNubia.IniciarSesionAdministrador(usuario, contraseña);
            if (resultado == true) {
                Administrador formularioInicioSesionAdministrador = new Administrador(null, true, this.LaNubia);
                formularioInicioSesionAdministrador.setVisible(true);
            }
        } catch (MisExcepciones ex) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error "+ex);
        }

    }//GEN-LAST:event_btnIniciarSesionAdministradorActionPerformed

