    public void guardar() {
        try {
            FileOutputStream archivo = new FileOutputStream("datos.txt");
            ObjectOutputStream oos = new ObjectOutputStream(archivo);
            oos.writeObject(LaNubia);
            oos.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    public void cargar() {
        try {
            FileInputStream archivo = new FileInputStream("datos.txt");
            try (ObjectInputStream ois = new ObjectInputStream(archivo)) {
                LaNubia = (AeropuertoControlador) ois.readObject();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

